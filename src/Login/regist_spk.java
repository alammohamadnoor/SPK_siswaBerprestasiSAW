    private void bsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsaveActionPerformed
        String sql = "insert into tb_admin values (?,?,?,?,?,?)";
        try{
            PreparedStatement st = conn.prepareStatement(sql);
            
            st.setString(1, txtusername.getText());
            st.setString(2, String.valueOf(txtpass.getPassword()));
            st.setString(3, txtnm.getText());
            st.setString(4, txtalamat.getText());
            st.setString(5, txtemail.getText());
            st.setString(6, txtnotelp.getText());
            
            st.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");
            clear();
            txtusername.requestFocus();
            
            
            }catch(SQLException ex){
            JOptionPane.showMessageDialog(this, "Data gagal Disimpan " +ex);
            }
    }//GEN-LAST:event_bsaveActionPerformed

